        function moveSurveyPart(_id){
            var offset = $("#" + _id).offset();
            $('html, body').animate({scrollTop : offset.top}, 400);
        }


		function submitForm(f)
		{
			if (f.name.value == "")
			{
				alert("이름을 입력해주세요");
				f.name.focus();
				return false;
			}

			if (f.mphone.value == "")
			{
				alert("전화번호를 입력해주세요");
				f.mphone.focus();
				return false;
			}

			if($("input[name=items1]:checked").val() == undefined){
			    alert("설문에 답해주세요.");
			    moveSurveyPart("survey1");
			    return false;
            }

            if($("input[name=items2]:checked").val() == undefined){
                alert("설문에 답해주세요.");
                moveSurveyPart("survey2");
                return false;
            }

            if($("input[name=items3]:checked").val() == undefined){
                alert("설문에 답해주세요.");
                moveSurveyPart("survey3");
                return false;
            }


			if(f.chkPrivateAgree.checked == false){
				alert("개인정보 수집 및 이용 동의에 체크해주세요");

				return false;
			}

			// 체크한 항목정보를 merge
			var etcInfo1 = "1. 목표 교정 시력은?\n";
			etcInfo1 += $("input[name=items1]:checked").val()+"\n\n";
            var etcInfo2 = "2. 라식 수술을 고민하는 이유는?\n";
            etcInfo2 += $("input[name=items2]:checked").val()+"\n\n";
            var etcInfo3 = "3. 해당 이벤트를 알게 된 경로?\n";
            etcInfo3 += $("input[name=items3]:checked").val();

            etcInfo = etcInfo1+etcInfo2+etcInfo3;
			$(".inqForm").find("input[name=etcInfo]").val(etcInfo);

			var data = $(".inqForm").serialize();

			//console.log(etcInfo);

			$.ajax({
				url : 'http://db.mdmarketing.co.kr/api/index.php?ctrl=inq.regist',
				type : 'post',
				dataType : 'json',
				data : data,
				success : function(data){
					if (data.returnCode == true){
						alert("감사합니다. 신청이 완료되었습니다.");
						document.inqForm.reset();

						//리스트 초기화
						$(".counselListArea .listArea").empty();
						counselListPageNum = 1;
						createCounselList();
					} else {
						alert(data.returnMsg);
					}
				},
				error : function(err){
					alert(err);
				},
			});
		}
